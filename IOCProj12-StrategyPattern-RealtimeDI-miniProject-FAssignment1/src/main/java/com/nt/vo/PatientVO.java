package com.nt.vo;

public class PatientVO {

	private String pname;
	private String padd;
	private String perdaycharge;
	private String nodays;
	
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPadd() {
		return padd;
	}
	public void setPadd(String padd) {
		this.padd = padd;
	}
	public String getPerdaycharge() {
		return perdaycharge;
	}
	public void setPerdaycharge(String perdaycharge) {
		this.perdaycharge = perdaycharge;
	}
	public String getNodays() {
		return nodays;
	}
	public void setNodays(String nodays) {
		this.nodays = nodays;
	}
	
}
